const Home = (props) => {
  const [jobs, setJobs] = useState([]);
  const [filterOpen, setFilterOpen] = useState(false);
  const [searchOptions, setSearchOptions] = useState({
    query: "",
    jobType: {
      fullTime: false,
      partTime: false,
      wfh: false,
    },
    salary: [0, 100],
    duration: "0",
    sort: {
      salary: {
        status: false,
        desc: false,
      },
      duration: {
        status: false,
        desc: false,
      },
      rating: {
        status: false,
        desc: false,
      },
    },
  });

  const setPopup = useContext(SetPopupContext);
  useEffect(() => {
    getData();
  }, []);

  const getData = () => {
    let searchParams = [];
    if (searchOptions.query !== "") {
      searchParams = [...searchParams, `q=${searchOptions.query}`];
    }
    if (searchOptions.jobType.fullTime) {
      searchParams = [...searchParams, `jobType=Full%20Time`];
    }
    if (searchOptions.jobType.partTime) {
      searchParams = [...searchParams, `jobType=Part%20Time`];
    }
    if (searchOptions.jobType.wfh) {
      searchParams = [...searchParams, `jobType=Work%20From%20Home`];
    }
    if (searchOptions.salary[0] != 0) {
      searchParams = [
        ...searchParams,
        `salaryMin=${searchOptions.salary[0] * 1000}`,
      ];
    }
    if (searchOptions.salary[1] != 100) {
      searchParams = [
        ...searchParams,
        `salaryMax=${searchOptions.salary[1] * 1000}`,
      ];
    }
    if (searchOptions.duration != "0") {
      searchParams = [...searchParams, `duration=${searchOptions.duration}`];
    }

    let asc = [],
      desc = [];

    Object.keys(searchOptions.sort).forEach((obj) => {
      const item = searchOptions.sort[obj];
      if (item.status) {
        if (item.desc) {
          desc = [...desc, `desc=${obj}`];
        } else {
          asc = [...asc, `asc=${obj}`];
        }
      }
    });
    searchParams = [...searchParams, ...asc, ...desc];
    const queryString = searchParams.join("&");
    console.log(queryString);
    let address = apiList.jobs;
    if (queryString !== "") {
      address = `${address}?${queryString}`;
    }

    axios
      .get(address, {
        headers: {
          Authorization: `Bearer ${localStorage.getItem("token")}`,
        },
      })
      .then((response) => {
        console.log(response.data);
        setJobs(
          response.data.filter((obj) => {
            const today = new Date();
            const deadline = new Date(obj.deadline);
            return deadline > today;
          })
        );
      })
      .catch((err) => {
        console.log(err.response.data);
        setPopup({
          open: true,
          severity: "error",
          message: "Error",
        });
      });
  };

  return (
    <>
      <Grid
        container
        item
        // class="mainGridContainer"
        direction="column"
        alignItems="center"
        style={{ padding: "30px", minHeight: "93vh" }}
      >
        <Grid
          item
          container
          // direction="column"
          // justify="center"
          // alignItems="center"
          className=" py-4 px-2 flex flex-row justify-between rounded-lg"
        >
          <div>
            <TextField
              sx={{
                "& .MuiOutlinedInput-root": {
                  outline: "none", // Remove outline
                  borderColor: "transparent", // Default no border
                  backgroundColor: "#fff", // Default background color

                  // Hover Styles
                  "&:hover .MuiOutlinedInput-notchedOutline": {
                    outline: "none", // Remove outline on hover
                    borderColor: "rgba(129, 140, 248)", // Light blue border on hover
                    boxShadow: "0 0 0 5px rgb(129 140 248 / 30%)", // Hover box shadow
                  },

                  // Focus Styles
                  "&.Mui-focused .MuiOutlinedInput-notchedOutline": {
                    outline: "none", // Remove outline on focus
                    borderColor: "rgba(129, 140, 248)", // Light blue border on focus
                    boxShadow: "0 0 0 5px rgb(129 140 248 / 30%)", // Focus box shadow
                  },
                },

                // Default styles for the outline
                "& .MuiOutlinedInput-notchedOutline": {
                  borderColor: "transparent", // No border by default
                },
              }}
              value={searchOptions.query}
              onChange={(event) =>
                setSearchOptions({
                  ...searchOptions,
                  query: event.target.value,
                })
              }
              onKeyPress={(ev) => {
                if (ev.key === "Enter") {
                  getData();
                }
              }}
              InputProps={{
                endAdornment: (
                  <InputAdornment>
                    <IconButton
                      className="bg-blue-500 "
                      onClick={() => getData()}
                    >
                      <span className="text-sm bg-blue-500 py-3 px-3 text-white rounded-2xl">
                        Find Job
                        {/* <SearchIcon /> */}
                      </span>
                    </IconButton>
                  </InputAdornment>
                ),
              }}
              id="custom-input"
              label="Search Jobs"
              variant="outlined"
            />
          </div>
          <Grid item>
            <span className="text-gray-600">Filter: </span>
            <IconButton onClick={() => setFilterOpen(true)}>
              <FilterListIcon />
            </IconButton>
          </Grid>
        </Grid>
        {/* <Grid item className="flex justify-between  w-full items-center mx-8">
          <Typography>Results</Typography>
        </Grid> */}

        <Grid container item class="boxOfContent" style={{ width: "100%" }}>
          {jobs.length > 0 ? (
            jobs.map((job) => {
              return <JobTile job={job} />;
            })
          ) : (
            <Typography variant="h5" style={{ textAlign: "center" }}>
              No jobs found
            </Typography>
          )}
        </Grid>
        <Grid item>
          <Pagination count={10} color="primary" />
        </Grid>
      </Grid>
      <FilterPopup
        open={filterOpen}
        searchOptions={searchOptions}
        setSearchOptions={setSearchOptions}
        handleClose={() => setFilterOpen(false)}
        getData={() => {
          getData();
          setFilterOpen(false);
        }}
      />
    </>
  );
};

export default Home;
